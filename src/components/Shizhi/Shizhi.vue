      if (ele == undefined) return false
      var boxTop
      boxTop = ele.getBoundingClientRect().top;
      var screenHeight = window.screen.availHeight;
      var realPenc = boxTop / screenHeight
      if (realPenc < perc && flag) {
        flag = false
